

   
a = Number(prompt("enter a number"))
 b = Number(prompt("enter a number"))
 c = prompt("enter an operator")

if (c == "+"){
    answer = (a + b)
}
else if (c == "*"){
    answer = (a * b)
}
 
else if (c == "-"){
    answer = (a - b)
}

else if(c == "/"){
    answer = (a/b)}




prompt(answer)